#include <iostream>

using namespace std;

int main()
{
  long long n;
  cin >> n;
  if (n <= 9)
    cout << n << endl;
  else if (n > 9 && n <= 99)
    cout << 9 + (n - 9) * 2 << endl;
  else if (n > 99 && n <= 999)
    cout << 9 + 180 + (n - 99) * 3 << endl;
  else if (n > 999 && n <= 9999)
    cout << 9 + 180 + 2700 + (n - 999) * 4 << endl;
  else if (n > 9999 && n <= 99999)
    cout << 9 + 180 + 2700 + 36000 + (n - 9999) * 5 << endl;
  else if (n > 99999 && n <= 999999)
    cout << 9 + 180 + 2700 + 36000 + 450000 + (n - 99999) * 6 << endl;
  else if (n > 999999 && n <= 9999999)
    cout << 9 + 180 + 2700 + 36000 + 450000 + 5400000 + (n - 999999) * 7 << endl;
  else if (n > 9999999 && n <= 99999999)
    cout << 9 + 180 + 2700 + 36000 + 450000 + 5400000 + 63000000 + (n - 9999999) * 8 << endl;
  else if (n > 99999999 && n <= 999999999)
    cout << 9 + 180 + 2700 + 36000 + 450000 + 5400000 + 63000000 + 720000000 + (n - 99999999) * 9 << endl;
  else if (n == 1000000000)
    cout << 9 + 180 + 2700 + 36000 + 450000 + 5400000 + 63000000 + 720000000 + 8100000000 + 10 << endl;
  return 0;
}
